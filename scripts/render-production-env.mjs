#!/usr/bin/env node
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'
import { mkdirSync, readFileSync, writeFileSync } from 'node:fs'

const DEFAULT_SCOPE = 'runtime'

function parseArgs(argv) {
  const args = {
    manifest: 'ops/env/production.env.json',
    output: '',
    scope: DEFAULT_SCOPE,
    enforce: null,
  }

  for (let i = 2; i < argv.length; i += 1) {
    const arg = argv[i]
    if (arg === '--manifest') {
      args.manifest = argv[++i]
    } else if (arg === '--output') {
      args.output = argv[++i]
    } else if (arg === '--scope') {
      args.scope = argv[++i]
    } else if (arg === '--enforce') {
      args.enforce = true
    } else if (arg === '--no-enforce') {
      args.enforce = false
    } else {
      throw new Error(`Unknown argument: ${arg}`)
    }
  }

  if (!args.output) throw new Error('--output is required')
  return args
}

function repoRoot() {
  return resolve(dirname(fileURLToPath(import.meta.url)), '..')
}

function loadManifest(path) {
  const manifestPath = resolve(repoRoot(), path)
  return JSON.parse(readFileSync(manifestPath, 'utf8'))
}

function entryScopes(entry) {
  if (Array.isArray(entry.scope)) return entry.scope
  return [entry.scope ?? DEFAULT_SCOPE]
}

function shouldRender(entry, scope) {
  return entryScopes(entry).includes(scope)
}

function normalizeValue(entry, value) {
  if (!entry.multiline) return value
  return value.replace(/\r\n/g, '\n').replace(/\\n/g, '\n')
}

function readEntryValue(entry, env) {
  const raw = env[entry.name]
  if (raw !== undefined) return { value: normalizeValue(entry, raw), source: 'env' }
  if (entry.default !== undefined) return { value: String(entry.default), source: 'default' }
  return { value: null, source: 'missing' }
}

function sentinelMatch(value, manifest, entry) {
  const normalized = value.trim().toLowerCase()
  const sentinels = [...(manifest.sentinels ?? []), ...(entry.sentinels ?? [])]

  for (const sentinel of sentinels) {
    const candidate = String(sentinel).trim().toLowerCase()
    if (!candidate) continue
    if (normalized === candidate) return sentinel
    if (['localhost', '127.0.0.1', 'placeholder', 'changeme'].includes(candidate) && normalized.includes(candidate)) {
      return sentinel
    }
  }

  return null
}

function quoteDotenvValue(value) {
  const normalized = value.replace(/\r\n/g, '\n')
  return `'${normalized.replace(/'/g, "\\'")}'`
}

function logStatus(level, message) {
  const annotationLevel = level === 'error' ? 'error' : level === 'warning' ? 'warning' : 'notice'
  console.log(`::${annotationLevel} title=production env::${message}`)
}

function render(manifest, { scope, enforce }, env) {
  const lines = [
    '# Generated by scripts/render-production-env.mjs from ops/env/production.env.json.',
    '# Do not hand-edit on the droplet; update GitHub Actions production secrets/variables instead.',
  ]
  const missing = []
  const sentinelDefaults = []
  const seen = new Set()

  for (const entry of manifest.entries ?? []) {
    if (!shouldRender(entry, scope)) continue
    if (seen.has(entry.name)) throw new Error(`Duplicate env entry: ${entry.name}`)
    seen.add(entry.name)

    const { value, source } = readEntryValue(entry, env)
    const empty = value === null || value.trim() === ''

    if (empty) {
      if (entry.required) {
        missing.push(entry)
        logStatus(enforce ? 'error' : 'warning', `${entry.name} is missing (${entry.source})`)
      }
      continue
    }

    const sentinel = sentinelMatch(value, manifest, entry)
    if (sentinel) {
      sentinelDefaults.push(entry)
      logStatus(enforce ? 'error' : 'warning', `${entry.name} matches sentinel ${sentinel}`)
    } else {
      logStatus('notice', `${entry.name} set from ${source === 'default' ? 'default' : entry.source}`)
    }

    lines.push(`${entry.name}=${quoteDotenvValue(value)}`)
  }

  if (enforce && (missing.length > 0 || sentinelDefaults.length > 0)) {
    const names = [...missing, ...sentinelDefaults].map((entry) => entry.name).join(', ')
    throw new Error(`Production env render failed: ${names}`)
  }

  return `${lines.join('\n')}\n`
}

function main() {
  const args = parseArgs(process.argv)
  const manifest = loadManifest(args.manifest)
  const enforceVariable = manifest.enforceVariable ?? 'PRODUCTION_ENV_RENDER_ENFORCE'
  const enforce = args.enforce ?? process.env[enforceVariable] === '1'
  const outputPath = resolve(process.cwd(), args.output)
  const body = render(manifest, { scope: args.scope, enforce }, process.env)

  mkdirSync(dirname(outputPath), { recursive: true })
  writeFileSync(outputPath, body, { mode: 0o600 })
  console.log(`::notice title=production env::rendered ${args.scope} env to ${outputPath}`)
}

try {
  main()
} catch (error) {
  console.error(error instanceof Error ? error.message : String(error))
  process.exit(1)
}
